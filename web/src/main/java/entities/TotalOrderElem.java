package entities;

public class TotalOrderElem {
	
	private int id;
	private int pos;
	
	public TotalOrderElem(int id, int pos) {
		super();
		this.id = id;
		this.pos = pos;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPos() {
		return pos;
	}
	public void setPos(int pos) {
		this.pos = pos;
	}
	
	
	
	
	
}